# Literal Examples

# Integer literal
a = 10
print("Integer Literal:")
print("a:", a)
print("Type of a:", type(a))
print()

# Floating-point literal
b = 10.5
print("Floating-point Literal:")
print("b:", b)
print("Type of b:", type(b))
print()

# Boolean literals
c = True
d = False
print("Boolean Literals:")
print("c:", c)
print("Type of c:", type(c))
print("d:", d)
print("Type of d:", type(d))
print()

# String literal
e = "Hello, World!"
print("String Literal:")
print("e:", e)
print("Type of e:", type(e))
print()

# Complex number literal
g = 3 + 4j
print("Complex Number Literal:")
print("g:", g)
print("Type of g:", type(g))
print()

# List literal
h = [1, 2, 3, 4, 5]
print("List Literal:")
print("h:", h)
print("Type of h:", type(h))
print()

# Tuple literal
i = (1, 2, 3, 4, 5)
print("Tuple Literal:")
print("i:", i)
print("Type of i:", type(i))
print()

# Dictionary literal
j = {'key1': 'value1', 'key2': 'value2', 'key3': 'value3'}
print("Dictionary Literal:")
print("j:", j)
print("Type of j:", type(j))
