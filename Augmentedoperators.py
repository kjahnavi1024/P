# Augmented Assignment Operators Program

# Augmented addition
a = 10
b = 5
print("Augmented addition of int and int")
a += b  # equivalent to a = a + b
print(f"a = {a}, type(a): {type(a)}\n")

# Augmented subtraction
a = 15
b = 7
print("Augmented subtraction of int and int")
a -= b  # equivalent to a = a - b
print(f"a = {a}, type(a): {type(a)}\n")

# Augmented multiplication
a = 10
b = 3.5
print("Augmented multiplication of int and float")
a *= b  # equivalent to a = a * b
print(f"a = {a}, type(a): {type(a)}\n")

# Augmented division
a = 20
b = 4.0
print("Augmented division of int and float")
a /= b  # equivalent to a = a / b
print(f"a = {a}, type(a): {type(a)}\n")

# Augmented modulus
a = 17
b = 3.5
print("Augmented modulus of int and float")
a %= b  # equivalent to a = a % b
print(f"a = {a}, type(a): {type(a)}\n")

# Augmented exponentiation
a = 2.0
b = 3
print("Augmented exponentiation of float and int")
a **= b  # equivalent to a = a ** b
print(f"a = {a}, type(a): {type(a)}\n")

# Augmented floor division
a = 19
b = 4.0
print("Augmented floor division of int and float")
a //= b  # equivalent to a = a // b
print(f"a = {a}, type(a): {type(a)}\n")

# Augmented addition with complex numbers
a = 3 + 4j
b = 1 + 2j
print("Augmented addition of complex numbers")
a += b  # equivalent to a = a + b
print(f"a = {a}, type(a): {type(a)}\n")
