# Comparison Operators
x = 10
y = 5

print("Comparison Operators:")
print("x == y:", x == y)
print("x != y:", x != y)
print("x > y:", x > y)
print("x < y:", x < y)
print("x >= y:", x >= y)
print("x <= y:", x <= y)
print()

# Logical Operators
a = True
b = False

print("Logical Operators:")
print("a and b:", a and b)
print("a or b:", a or b)
print("not a:", not a)
print()

# Bitwise Operators
p = 5  # 101 in binary
q = 3  # 011 in binary

print("Bitwise Operators:")
print("p & q:", p & q)  # Bitwise AND
print("p | q:", p | q)  # Bitwise OR
print("p ^ q:", p ^ q)  # Bitwise XOR
print("~p:", ~p)  # Bitwise NOT
print("p << 1:", p << 1)  # Left shift
print("p >> 1:", p >> 1)  # Right shift
print()

# Membership Operators
list_example = [1, 2, 3, 4, 5]

print("Membership Operators:")
print("2 in list_example:", 2 in list_example)
print("6 not in list_example:", 6 not in list_example)
print()

# Identity Operators
m = 10
n = 10.0

print("Identity Operators:")
print("m is n:", m is n)
print("m is not n:", m is not n)
