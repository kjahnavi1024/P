# Assignment Operators

# = (Simple Assignment)
x = 10
y = 5
print("Initial values: x =", x, "y =", y)

# += (Add and Assign)
x += 3
y += x
print("After x += 3, x =", x)
print("After y += x, y =", y)

# -= (Subtract and Assign)
x -= 2
y -= x
print("After x -= 2, x =", x)
print("After y -= x, y =", y)

# *= (Multiply and Assign)
x *= 5
y *= x
print("After x *= 5, x =", x)
print("After y *= x, y =", y)

# /= (Divide and Assign)
x /= 2
y /= x
print("After x /= 2, x =", x)
print("After y /= x, y =", y)

# %= (Modulus and Assign)
x %= 3
y %= x
print("After x %= 3, x =", x)
print("After y %= x, y =", y)

# //= (Floor Division and Assign)
x //= 2
y //= x
print("After x //= 2, x =", x)
print("After y //= x, y =", y)

# **= (Exponent and Assign)
x **= 3
y **= x
print("After x **= 3, x =", x)
print("After y **= x, y =", y)
